    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'name' => ['required'],
            'artist_pic' => ['required', 'image', 'max:1990'],
        ]);
        
        $artist = new Artist;
        $artist->name = $request->input('name');    

        if($request->hasFile('artist_pic')){
            // Get filename with the extension
            $filenameWithExt = $request->file('artist_pic')->getClientOriginalName();
            // Get just filename
            $filename = pathinfo($filenameWithExt, PATHINFO_FILENAME);
            // Get just ext
            $extension = $request->file('artist_pic')->getClientOriginalExtension();
            // Filename to store
            $fileNameToStore= $filename.'_'.time().'.'.$extension;
            // Upload Image
            $path = $request->file('artist_pic')->storeAs('public/images/artist_pic/', $fileNameToStore);
        } else {
            $fileNameToStore = 'noimage.jpg';
        }   
        $artist->artist_pic = $fileNameToStore;
        $artist->save();

        $context = array(
            'success' => 'Artist Added',
        );
        return redirect('/')->with($context);
    }

